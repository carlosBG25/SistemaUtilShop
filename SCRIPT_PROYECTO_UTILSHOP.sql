-- CREAMOS EL DISPARADOR PARA LLENAR EL PRECIOTOTAL CADA VEZ QUE HACEMOS UN INSERT
CREATE TRIGGER CalculosFactura
ON TB_FACTURA
AFTER INSERT
AS
BEGIN
    -- ACTUALIZAR EL CAMPO STOCK DE LA TABLA PRODUCTOS

    UPDATE p  SET p.stock = p.stock - i.cantidad
    FROM TB_PRODUCTO p
    INNER JOIN inserted i
    ON p.Id_producto = i.Id_factura_Producto

    -- ACTUALIZAR EL CAMPO PRECIOTOTAL EN LOS REGISTROS INSERTADOS

    UPDATE f SET f.precioTotal = i.cantidad * i.precioUnitario    
    FROM TB_FACTURA f 
    INNER JOIN inserted i
    ON f.id_factura = i.id_factura
END
GO
