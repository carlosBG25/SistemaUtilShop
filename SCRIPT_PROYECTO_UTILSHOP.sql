CREATE PROC SP_CODIGO_TRABAJADOR
AS
BEGIN
    DECLARE @codigo INT,
            @concat VARCHAR(6),
			@default VARCHAR(6)

    SET @concat = 'TR';

    SET @codigo = (SELECT TOP 1 TRY_CONVERT(INT,SUBSTRING(Id_trabajador,3,4)) as codigo FROM TB_TRABAJADOR
    ORDER BY codigo DESC) + 1

	IF(@codigo IS NULL) -- SI NO HAY REGISTROS
    BEGIN
		SET @default = 'TR1001';
		SELECT @default as codigo
	END
	ELSE
		SET @concat = @concat + TRY_CONVERT(CHAR,@codigo);
		SELECT @concat as codigo
END
GO

CREATE PROC SP_CODIGO_FACTURA
AS
BEGIN
    DECLARE @codigo INT
    DECLARE @default INT

    SET @codigo = (SELECT TOP 1 numerofactura as codigo FROM TB_FACTURA
    ORDER BY codigo DESC) + 1


