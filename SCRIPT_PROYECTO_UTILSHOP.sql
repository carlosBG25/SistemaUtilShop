CREATE VIEW V_PRODUCTOS
AS
    SELECT 
        P.Id_producto,
        P.nombre,
        C.descripcion as categoria,
        PV.empresa as proveedor,
        P.stock,
        P.precioUnitario 
    FROM TB_PRODUCTO P
    INNER JOIN TB_CATEGORIA C
    ON P.id_productoCateg = C.Id_categoria
    INNER JOIN TB_PROVEEDOR PV
    ON P.id_productoProveedor = PV.Id_proveedor
    WHERE P.stock >=1
GO

CREATE VIEW V_LISTAR_REGISTRADORES
AS
    SELECT Id_trabajador,
    CASE 
        WHEN 
            CHARINDEX(' ', nombres) > 0 -- REGISTROS CON 2 NOMBRES
        THEN
            CONCAT(SUBSTRING(nombres, 1, CHARINDEX(' ', nombres) - 1), ' ', ap_paterno, ' ', ap_materno)
        ELSE
            CONCAT(nombres,' ',ap_paterno,' ',ap_materno) -- REGISTROS CON UN SOLO NOMBRE
    END as nombres
    FROM TB_TRABAJADOR
    WHERE Id_trabTipoUser = 1
    AND Id_trabajadorEstado = 1
GO

