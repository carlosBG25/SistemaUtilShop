CREATE PROC SP_FILTRAR_FACTURAS
(
@valor VARCHAR(15)
)
AS
BEGIN

    SELECT TOP 200
	F.id_factura as Codigo,
	F.dni_Cliente as Cliente,
	CONCAT(T.nombres,' ',T.ap_paterno,' ',T.ap_materno) as Digitador,
	P.Id_producto AS 'Codigo del Producto',
	P.nombre as 'Nombre',
	F.cantidad as Cantidad,
	F.precioUnitario AS 'Precio Unitario',
	F.precioTotal AS 'Precio Total',
	F.numerofactura as 'Numero de Factura',
	F.fecha_compra as 'Fecha de Venta'
	FROM TB_FACTURA F
	INNER JOIN TB_TRABAJADOR T
	ON F.Id_factura_Trabajador = T.Id_trabajador
	INNER JOIN TB_PRODUCTO P
	ON F.id_factura_producto = P.Id_producto
    WHERE TRY_CONVERT(VARCHAR,F.dni_Cliente) LIKE  + TRY_CONVERT(VARCHAR,@valor) + '%'
    ORDER BY 1

END
GO

CREATE PROC SP_FILTRAR_TRABAJADOR
(
@valor VARCHAR(20)
)
AS
BEGIN
    SELECT TOP 200
        T.Id_trabajador,
        T.login_trabajador,
        T.[PASSWORD],
        T.nombres,
        T.ap_paterno,
        T.ap_materno,
        TD.Tipo_Doc,
        T.num_doc,
        TU.nombre as tipo_usuario,
        TE.estado 
    FROM TB_TRABAJADOR T 
    INNER JOIN TB_TIPO_DOCUMENTO TD
    ON T.id_trabajadorDoc = TD.Id_doc 
    INNER JOIN TB_TIPO_USUARIO TU
    ON T.Id_trabTipoUser = TU.Id_tipoUser 
    INNER JOIN TB_ESTADO TE
    ON T.Id_trabajadorEstado = TE.Id_estado
    WHERE UPPER(T.ap_paterno) LIKE + UPPER(@valor) + '%'
    ORDER BY 1
END
GO

