CREATE PROC SP_LISTAR_FACTURA
AS
BEGIN
    SELECT 
	F.id_factura as Codigo,
	F.dni_Cliente as Cliente,
	CONCAT(T.nombres,' ',T.ap_paterno,' ',T.ap_materno) as Digitador,
	P.Id_producto AS 'Codigo del Producto',
	P.nombre as 'Nombre',
	F.cantidad as Cantidad,
	F.precioUnitario AS 'Precio Unitario',
	F.precioTotal AS 'Precio Total',
	F.numerofactura as 'Numero de Factura',
	F.fecha_compra as 'Fecha de Venta'

	FROM TB_FACTURA F
	INNER JOIN TB_TRABAJADOR T
	ON F.Id_factura_Trabajador = T.Id_trabajador
	INNER JOIN TB_PRODUCTO P
	ON F.id_factura_producto = P.Id_producto
    ORDER BY 1
END
GO


