-- ===================== CREAR TABLAS =====================
DROP TABLE IF EXISTS TB_TIPO_USUARIO
GO
CREATE TABLE TB_TIPO_USUARIO
(
    Id_tipoUser INT IDENTITY(1,1) not null,
    nombre VARCHAR(50)

)
GO

DROP TABLE IF EXISTS TB_ESTADO
GO
CREATE TABLE TB_ESTADO
(
    Id_estado INT IDENTITY(1,1) not null,
    estado VARCHAR(30)

)
GO

DROP TABLE IF EXISTS TB_TIPO_DOCUMENTO
GO
CREATE TABLE TB_TIPO_DOCUMENTO
(
    Id_doc VARCHAR(7) not null,
    Tipo_Doc VARCHAR(4),
    Descripcion VARCHAR(35)
)
GO

DROP TABLE IF EXISTS TB_PROVEEDOR
GO
CREATE TABLE TB_PROVEEDOR
(
    Id_proveedor INT IDENTITY(1,1) not null,
    empresa VARCHAR(20),
    persona VARCHAR(35),
    celular INT,
    correo VARCHAR(40)
)
GO

DROP TABLE IF EXISTS TB_CATEGORIA
GO
CREATE TABLE TB_CATEGORIA
(
    Id_categoria INT IDENTITY(1,1) not null,
    descripcion VARCHAR(40),

)
GO

DROP TABLE IF EXISTS TB_TRABAJADOR
GO
CREATE TABLE TB_TRABAJADOR
(
    Id_trabajador VARCHAR(6) not null,
    login_trabajador VARCHAR(10) not null,
    PASSWORD VARCHAR(20) not null,
    nombres VARCHAR (30),
    ap_paterno VARCHAR(20),
    ap_materno VARCHAR(20),
    id_trabajadorDoc VARCHAR(7) not null,
    num_doc VARCHAR(15),
    Id_trabTipoUser INT not null,
    Id_trabajadorEstado INT not null
)
GO

DROP TABLE IF EXISTS TB_PRODUCTO
GO
CREATE TABLE TB_PRODUCTO
(
    Id_producto VARCHAR(10) not null,
    nombre VARCHAR(300),
    id_productoCateg INT not null,
    id_productoProveedor INT not null,
    stock INT,
    precioUnitario FLOAT,

)
GO

DROP TABLE IF EXISTS TB_FACTURA
GO

CREATE TABLE TB_FACTURA
(
    id_factura INT IDENTITY(110000001,1) NOT NULL,
    Id_factura_Doc VARCHAR(7) NOT NULL,
    dni_Cliente VARCHAR(15),
    Id_factura_Trabajador VARCHAR(6) NOT NULL,
    id_factura_producto VARCHAR(10) NOT NULL,
    cantidad INT,
    precioUnitario FLOAT,
    precioTotal FLOAT,
    numerofactura INT NOT NULL DEFAULT 100000000,
    fecha_compra DATETIME
)
GO

